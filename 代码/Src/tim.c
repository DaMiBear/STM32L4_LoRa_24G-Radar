//此回调函数默认是__weak定义的，所以我们在这里需要重新定义一下
//而且此回调函数是所有定时器共用的，所以我们需要先通过if (htim->Instance == htim3.Instance)判断它是哪个定时器中断在调用
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	static int i = 0;
    if (htim->Instance == htim3.Instance)   
    {                  
        /*每隔Ts采样一次数据*/
			ADC_ConvertedDate1[i] = ADC_ConvertedValue[0];
			ADC_ConvertedDate2[i] = ADC_ConvertedValue[1];
			i++;
			if(i == FFT_LENGTH)	//一共采样FFT_LENGTH个数据
			{
				i = 0;
				enableFFTState = 1;	//FFT_LENGTH个数采集完毕可以进行FFT	在进行FFT的时候会关闭定时器中断
			}
    }
}
